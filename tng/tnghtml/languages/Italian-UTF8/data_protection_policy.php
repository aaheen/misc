<h3>Cookies</h3>
<p>Questo sito utilizza i cookies. I cookies non danneggiano il tuo computer e non contengono virus. I cookies svolgono funzioni importanti 
	per l'operatività di questo sito. I cookies sono piccoli file di testo memorizzati nel tuo computer dal 
	tuo browser.</p>

<p>La maggior parte dei cookies che utilizziamo sono "cookies di sessione". Questi vengono automaticamente cancellati quando chiudi il tuo browser. 
	Altri cookies restano nel tuo device fino a che non li cancelli. Questi cookies ci permettono di riconoscere il tuo browser alla prossima visita 
	e ripristinano alcune delle tue preferenze (ad esempio la tua lingua preferita).</p>

<p>Puoi configurare i parametri del tuo browser per essere informato dei settaggi dei cookies ed autorizzare i cookies sono in determinati casi. 
	Disabilitare i cookies può limitare le funzionalità di questo sito web.</p>

