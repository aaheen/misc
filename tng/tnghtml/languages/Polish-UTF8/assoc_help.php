		<a name="what"><p class="subheadbold">Co to są asocjacje?</p></a>

		<p><strong>Asocjacja</strong> jest zapisem relacji między dwiema osobami w Twojej bazie danych, które
                nie muszą być odzwierciedlone w strukturze Twego drzewa genealogicznego. Dwie osoby (np. chrześniak i matka chrzestna, albo osoba zmarła i świadek zgonu),
                które są ze sobą powiązane w asocjacji, nie muszą mieć między sobą jakichkolwiek powiązań rodzinnych.</p>

