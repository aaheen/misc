		<a name="add"><p class="subheadbold">Dodawanie/Edycja/Usuwanie asocjacji</p></a>

		<p>Aby dodawać, edytować lub usuwać asocjacje, zlokalizuj osobę w Administracja / Osoby i edytuj poszczególne zapisy,
                a następnie kliknij na ikonkę <em>Asocjacje</em> na górze ekranu (jeśli istnieją już jakieś asocjacje, ikonka będzie oznaczona zieloną kropką).
                Po kliknięciu ikonki, pojawi się małe okienko (popup), w którym zobaczysz wszystkie istniejące dla danej osoby asocjacje.</p>

		<p>Aby dodać nową asocjację, kliknij na "Dodaj nowe" i wypełnij formularz.</p>

		<p>Aby edytować lub usuwać istniejące asocjacje, kliknij na odpowiednią ikonkę obok tej asocjacji.</p>

		<p>Podczas dodawania lub edycji asocjacji dostępne są następujące pola:</p>

		<span class="optionhead">ID osoby</span>
		<p>Wprowadź ID osoby, która ma uzyskać asocjację z wybraną osobą, lub kliknij ikonę "Znajdź" aby wyszukać jej ID.</p>

		<span class="optionhead">Wzajemny stosunek</span>
		<p>Wprowadź charakter asocjacji między tymi osobami. Na przykład, <em>ojciec chrzestny</em>, <em>świadek</em> itp.</p>

		<p>Po dodaniu, edycji lub usunięciu asocjacji kliknij przycisk "Koniec", aby zamknąć okno.</p>
	
