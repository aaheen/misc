<h3>Cookies</h3>
<p>Ta strona korzysta z plików cookie. Pliki cookie nie szkodzą komputerowi i nie zawierają wirusów. Pliki cookie pełnią ważne funkcje w działaniu tej strony.</br>
Pliki cookie to małe pliki tekstowe przechowywane na komputerze użytkownika przez przeglądarkę.</p>

<p>Większość plików cookie, których używamy, to "cookie sesji". Są one automatycznie usuwane po zamknięciu przeglądarki. Inne pliki cookie
pozostaną zapisane na Twoim urządzeniu, dopóki ich nie usuniesz.</br>
Te pliki cookie pozwalają nam rozpoznać Twoją przeglądarkę przy następnej wizycie i przywrócić niektóre z Twoich preferencji (takich jak preferowany język i zakładki).</p>

<p>Możesz skonfigurować ustawienia przeglądarki tak, aby informowała o ustawieniach plików cookie i zezwalała na zapis plików cookie tylko w niektórych przypadkach.</br>
Wyłączenie plików cookie może ograniczyć funkcjonalność tej witryny.</p>

